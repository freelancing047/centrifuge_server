package csi.server.common.model.map;

public class PlaceSizeInfo {
	private int min;
	private int max;
	public PlaceSizeInfo() {
	}
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
}
