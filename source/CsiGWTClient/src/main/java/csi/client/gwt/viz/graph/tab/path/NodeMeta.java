package csi.client.gwt.viz.graph.tab.path;

public class NodeMeta {

    public String id;

    public String name;

    public NodeMeta(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
