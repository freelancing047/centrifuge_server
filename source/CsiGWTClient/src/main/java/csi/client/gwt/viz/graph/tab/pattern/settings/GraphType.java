package csi.client.gwt.viz.graph.tab.pattern.settings;

public class GraphType {
    private String name;
    private String uuid;

    public GraphType() {
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
